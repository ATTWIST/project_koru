function getPageLang() {
  let saved = "ko";
  try {
    saved = localStorage.getItem("jlife-lang") || "ko";
  } catch {
    saved = "ko";
  }
  return ["ko", "ja", "en"].includes(saved) ? saved : "ko";
}

function setPageLang(lang) {
  const page = document.body.dataset.page;
  const copy = { ...sharedCopy[lang], ...pageCopy[page][lang] };
  document.documentElement.lang = lang === "ko" ? "ko" : lang === "ja" ? "ja" : "en";
  document.title = copy.title;
  document.querySelector('meta[name="description"]')?.setAttribute("content", copy.description);

  document.querySelectorAll("[data-page-i18n]").forEach((node) => {
    const value = copy[node.dataset.pageI18n];
    if (value) node.textContent = value;
  });

  document.querySelectorAll("[data-page-lang]").forEach((button) => {
    button.classList.toggle("active", button.dataset.pageLang === lang);
  });

  try {
    localStorage.setItem("jlife-lang", lang);
  } catch {
    // Language still changes on the current page when storage is blocked.
  }
}

document.querySelectorAll("[data-page-lang]").forEach((button) => {
  button.addEventListener("click", () => {
    setPageLang(button.dataset.pageLang);
    if (typeof window.gtag === "function") {
      window.gtag("event", "page_language_switch", {
        language: button.dataset.pageLang,
        page: document.body.dataset.page,
      });
    }
  });
});

setPageLang(getPageLang());
